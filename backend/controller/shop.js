router.get(
  "/get-first-seller-phone",
  catchAsyncErrors(async (req, res, next) => {
    try {
      // Find the first seller, sorted by createdAt (earliest first)
      const seller = await Shop.findOne().sort({ createdAt: 1 }).select("phoneNumber");

      if (!seller) {
        return next(new ErrorHandler("No sellers found in the database", 404));
      }

      res.status(200).json({
        success: true,
        phoneNumber: seller.phoneNumber,
      });
    } catch (error) {
      console.error("Error fetching first seller phone number:", error);
      return next(new ErrorHandler(error.message, 500));
    }
  })
);
// create shop
router.post("/create-shop", catchAsyncErrors(async (req, res, next) => {
  try {
    const { email } = req.body;
    const sellerEmail = await Shop.findOne({ email });
    if (sellerEmail) {
      return next(new ErrorHandler("User already exists", 400));
    }

    const seller = {
      name: req.body.name,
      email: email,
      password: req.body.password,
      address: req.body.address,
      phoneNumber: req.body.phoneNumber,
      zipCode: req.body.zipCode,
    };

    const newSeller = await Shop.create({
      name: seller.name,
      email: seller.email,
      password: seller.password,
      address: seller.address,
      phoneNumber: seller.phoneNumber,
      zipCode: seller.zipCode
    });

    sendShopToken(newSeller, 201, res);
  } catch (error) {
    return next(new ErrorHandler(error.message, 500));
  }
}));

// login shop
router.post(
  "/login-shop",
  catchAsyncErrors(async (req, res, next) => {
    try {
      const { email, password } = req.body;

      if (!email || !password) {
        return next(new ErrorHandler("Please provide the all fields!", 400));
      }

      const user = await Shop.findOne({ email }).select("+password");

      if (!user) {
        return next(new ErrorHandler("User doesn't exists!", 400));
      }

      const isPasswordValid = await user.comparePassword(password);

      if (!isPasswordValid) {
        return next(
          new ErrorHandler("Please provide the correct information", 400)
        );
      }

      sendShopToken(user, 201, res);
    } catch (error) {
      return next(new ErrorHandler(error.message, 500));
    }
  })
);

